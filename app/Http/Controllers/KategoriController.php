    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validatedData = $request->validate([
            'nama_kategori' => 'required',
        ]);
        // $menu->save();
        Kategori::create($validatedData);
        return redirect()->route('admin.kategori')->with('success', 'Success Creating Menu');
    }

    /**
     * Display the specified resource.
     *
     * @param  \App\Models\Kategori  $kategori
     * @return \Illuminate\Http\Response
     */
    public function show(Kategori $kategori)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  \App\Models\Kategori  $kategori
     * @return \Illuminate\Http\Response
     */
    public function edit(Kategori $kategori)
    {
        //
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Models\Kategori  $kategori
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Kategori $kategori)
    {
        // ddd($request);
        $validatedData = $request->validate([
            'nama_kategori' => 'required',
        ]);
        Kategori::where('id_kategori', $request->id_kategori)->update($validatedData);
        // Menu::where('id_menu', $menu->id_menu)->update($validatedData);
        return redirect()->route('admin.kategori')->with('success', 'Success Editing Menu');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Models\Kategori  $kategori
     * @return \Illuminate\Http\Response
     */
    public function destroy(Kategori $kategori)
    {
        Kategori::destroy($kategori->id_kategori);
        return redirect()->route('admin.menu')->with('success', 'Success Deleting Menu');
    }
}
